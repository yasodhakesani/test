
#range() function returns a sequence of numbers, 
# starting from 0 by default, and increments by 1 (by default), and ends at a specified number.
for x in range(5):
    print(x)

#start and end index you have to pass and increments by 1 (by default)
for x in range(2,6):
    print(x)

#start and end index you have to pass and increments by 2 as we are giving the increment sequence
for x in range(2,10,2):
    print(x)

